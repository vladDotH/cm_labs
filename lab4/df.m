function d = df(x, n = 1)
  Z = 2*x - 3;
  F = (x .^ 2 - 3 * x + 92);
  switch (n)
    case 1
      d = -2000 .* Z ./ F.^2;
    case 2
      d = 2000 .* (2*Z.^2 ./ F.^3 .- 2 ./ F.^2);
    case 3
      d = 2000 .* (12*Z ./ F.^3 .- 6*Z.^3 ./ F.^4 );
    case 4
      d = 2000 .* (24*Z.^4 ./ F.^5 .- 72*Z.^2 ./ F.^4 .+ 24 ./ F.^3);
    case 5
      d = 2000 .* (-120*Z.^5 ./ F.^6 .+ 480*Z.^3 ./ F.^5 .- 360*Z ./ F.^4);
    case 6
      d = 2000 .* (720*Z.^6 ./ F.^7 .- 3600*Z.^4 ./ F.^6 .+ 4320*Z.^2 ./ F.^5 .- 720 ./ F.^4);
  endswitch
endfunction
